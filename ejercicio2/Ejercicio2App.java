package UD4.ejercicio2;


public class Ejercicio2App {
	
	public static void main (String args[]) {
		
		int n = 10;
		double a = 17.7;
		char c = 'z';
		
		System.out.println("La variable n es: "+n);
		System.out.println("La variable z es: "+a);
		System.out.println("La variable c es: "+c);
		
		System.out.println("La suma n + a es: "+(n+a));
		
		System.out.println("La diferencia a - n es: "+(a-n));
		
		System.out.println("El valor numerico del caracter c es: "+(int)c);
	}
}
